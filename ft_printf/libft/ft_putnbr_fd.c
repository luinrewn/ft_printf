#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	temp;

	if (fd < 0)
		return ;
	if (n == INT_MIN)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		n = -n;
		write(fd, "-", 1);
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	temp = (n % 10) + '0';
	write(fd, &temp, 1);
}
